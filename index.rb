# parameter

def praise_person(name)
    puts "#{name} is amazing"
    puts "#{name} is talented"
    puts "#{name} is brilliant"
end

praise_person("Muhammad")
praise_person("Hassan")
praise_person("Umar")

# output
# Muhammad is amazing
# Muhammad is talented
# Muhammad is brilliant
# Hassan is amazing
# Hassan is talented
# Hassan is brilliant
# Umar is amazing
# Umar is talented
# Umar is brilliant